void send_sensor_values_over_uart(int motor_speed, int steering_angle, float pressure, float temperature, float humidity) {
    char buffer[100];  // Uygun bir boyut seçilmelidir
    int temperature_int = (int)(temperature * 10.0);  // Multiply by 10 to keep one decimal place
    int pressure_int  = (int)(pressure * 10.0);
    int humidity_int = (int)(humidity * 10.0);
    // Integer değerlerini karakter dizisine dönüştür ve uygun formatta birleştir
    sprintf(buffer, "Motor: %d RPM, Steering: %d deg, Pressure: %d hPa, Temp: %dC, Humidity: %d%%\r\n",
            motor_speed, steering_angle, pressure_int, temperature_int, humidity_int);

    // UART üzerinden gönder
    HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);
}


